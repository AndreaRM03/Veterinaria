    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        String nombre = txtfNombre.getText();
        String raza = txtfRaza.getText();
        String color = txtfColor.getText();
        double peso = Double.valueOf(txtfPeso.getText());
        double altura = Double.valueOf(txtfAltura.getText());
        String dueno = txtfDueno.getText();
        
        Perritos perrito = new Perritos(nombre, raza, color, peso, altura, dueno);
        PerritosDAO perritosDAO = new PerritosDAO();
        int resultado = perritosDAO.insertarPerrito(perrito);
        
        if(resultado == 1){
            System.out.println("El perrito se creo exitosamente");
            VistaPerritos vistaPerritos = new VistaPerritos();
            vistaPerritos.setVisible(true);
            dispose();
        }else{
            System.out.println("No se pudo crear el perrito");
        }
    }//GEN-LAST:event_btnCrearActionPerformed

